# 1.isdigit,字符串是否是纯数字组成
s1 = "123456"
print(s1.isdigit())
s11 = "123hello"
print(s11.isdigit())

# 2.strip,消除空格
s2 = "    Hello World    "
print(s2.strip())

# 3.upper,字符串全部大写
s3 = "hello"
print(s3.upper())
s33 = "Hello"
print(s33.upper())

# 4.lower，字符串全小写
s4 = "HELLO"
print(s4.lower())
s44 = "Hello"
print(s4.lower())

